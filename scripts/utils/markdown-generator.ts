const HF_DATASET_URL =
  "https://huggingface.co/datasets/Goku-OpenLab/gpt-image-2-prompts-datasets";

function generateDatasetSection(total: number, locale: string): string {
  const formatted = new Intl.NumberFormat(locale === "zh" ? "zh-CN" : "en").format(total);
  return `## 💾 ${t("dataset", locale)}

<div align="center">

[![🤗 HuggingFace](https://img.shields.io/badge/🤗%20HuggingFace-${t("datasetDownload", locale)}-blue)](${HF_DATASET_URL})

</div>

${t("datasetDesc1", locale)} ${formatted}${t("datasetDesc2", locale)}

---
`;
}

function generateStats(total: number, locale: string): string {
  const now = new Date().toLocaleString(locale, {
    timeZone: "UTC",
    dateStyle: "full",
    timeStyle: "long",
  });

  return `## 📊 ${t("stats", locale)}

<div align="center">

| ${t("metric", locale)} | ${t("count", locale)} |
|--------|-------|
| 📝 ${t("totalPrompts", locale)} | **${total}** |
| 🔄 ${t("lastUpdated", locale)} | **${now}** |

</div>

---
`;
}

function generatePromptSection(
  prompt: Prompt,
  index: number,
  locale: string
): string {
  const rawContent = prompt.content || prompt.translatedContent;
  const promptContent = cleanPromptContent(rawContent);
  const hasArguments = promptContent.includes("{argument");

  let md = `### No. ${index + 1}: ${prompt.title}\n\n`;

  md += `![Language-${prompt.language.toUpperCase()}](https://img.shields.io/badge/Language-${prompt.language.toUpperCase()}-blue)\n`;

  if (prompt.featured) {
    md += `![Featured](https://img.shields.io/badge/⭐-Featured-gold)\n`;
  }

  md += `\n#### 📖 ${t("description", locale)}\n\n${prompt.description}\n\n`;
  md += `#### 📝 ${t("prompt", locale)}\n\n\`\`\`\n${promptContent}\n\`\`\`\n\n`;

  if (prompt.sourceMedia && prompt.sourceMedia.length > 0) {
    md += `#### 🖼️ ${t("generatedImages", locale)}\n\n`;

    prompt.sourceMedia.forEach((imageUrl, imgIndex) => {
      md += `##### Image ${imgIndex + 1}\n\n`;
      md += `<div align="center">\n`;
      md += `<img src="${imageUrl}" width="600" alt="${prompt.title} - Image ${imgIndex + 1}">\n`;
      md += `</div>\n\n`;
    });
  }

  md += `**[${t("tryItNow", locale)}](${APP_HOME_URL})**\n\n`;
  md += `---\n\n`;

  return md;
}

function generatePromptList(prompts: Prompt[], locale: string): string {
  if (prompts.length === 0) return "";

  let md = `## 📋 ${t("allPrompts", locale)}\n\n`;

  prompts.forEach((prompt, index) => {
    md += generatePromptSection(prompt, index, locale);
  });

  return md;
}

function generateFooter(hiddenCount: number, locale: string): string {
  const now = new Date().toLocaleString(locale, {
    timeZone: "UTC",
    dateStyle: "full",
    timeStyle: "long",
  });

  let md = "";

  if (hiddenCount > 0) {
    md += `## 📚 ${hiddenCount} ${t("moreNotShown", locale)}\n\n`;
    md += `<div align="center">\n\n`;
    md += `${t("limitNote", locale)} ${MAX_PROMPTS_TO_DISPLAY} ${t("limitNoteEnd", locale)}\n\n`;
    md += `**[👉 ${t("viewInGallery", locale)}](${APP_HOME_URL})**\n\n`;
    md += `</div>\n\n`;
  }

  md += `---\n\n`;
  md += `## 📄 ${t("license", locale)}\n\n`;
  md += `${t("licensedUnder", locale)}\n\n`;
  md += `---\n\n`;
  md += `## 🙏 ${t("acknowledgements", locale)}\n\n`;
  md += `- [Goku Prompt Hub](https://prompthub.gokuscraper.com)\n\n`;
  md += `---\n\n`;
  md += `<div align="center">\n\n`;
  md += `**[🌐 ${t("viewInGallery", locale)}](${APP_HOME_URL})** • `;
  md += `**[⭐ ${t("starRepo", locale)}](https://github.com/goku-open/awesome-gpt-image-2-prompts)**\n\n`;
  md += `<sub>🤖 ${t("autoGenerated", locale)} ${now}</sub>\n\n`;
  md += `</div>\n`;

  return md;
}
