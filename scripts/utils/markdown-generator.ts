const translations: Record<string, typeof en> = { en, zh };
const t = (key: keyof typeof en, locale: string): string => {
  const lang = translations[locale] || en;
  return lang[key] ?? en[key];
};

function cleanPromptContent(content: string): string {
  if (!content) return content;
  let cleaned = content;
  cleaned = cleaned.replace(/^```[\w-]*\s*\n?/im, "");
  cleaned = cleaned.replace(/\n?```\s*$/im, "");
  cleaned = cleaned.replace(/\n```[\w-]*\s*\n/g, "\n");
  cleaned = cleaned.trim();
  return cleaned;
}

function generateLanguageNav(currentLocale: string): string {
  const badges = SUPPORTED_LANGUAGES.map((lang) => {
    const isCurrent = lang.code === currentLocale;
    const color = isCurrent ? "brightgreen" : "lightgrey";
    const text = isCurrent ? "Current" : "Click%20to%20View";
    const link = lang.readmeFileName;
    const safeName = encodeURIComponent(lang.name);
    return `[![${lang.name}](https://img.shields.io/badge/${safeName}-${text}-${color})](${link})`;
  });

  return badges.join(" ") + "\n\n";
}

export function generateMarkdown(
  prompts: Prompt[],
  total: number,
  locale: string = "en"
): string {
  const displayed = prompts.slice(0, MAX_PROMPTS_TO_DISPLAY);
  const hiddenCount = total - displayed.length;

  let md = generateHeader(locale);
  md += generateGalleryPreview(locale);
  md += generateDatasetSection(total, locale);
  md += generateStats(total, locale);
  md += generatePromptList(displayed, locale);
  md += generateFooter(hiddenCount, locale);

  return md;
}

